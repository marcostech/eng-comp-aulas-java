    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        var portaSelecionada = portasSerial[jComboBox1.getSelectedIndex()-1];        
        portaSelecionada.flushIOBuffers();
        byte[] readBuffer = new byte[100];
        portaSelecionada.readBytes(readBuffer, readBuffer.length);
        //Convert bytes to String
        String dataRead = ""; 
        try {
            dataRead = new String(readBuffer, "UTF-8");
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Tela01.class.getName()).log(Level.SEVERE, null, ex);
        }
        String dataPacket = dataRead.substring(dataRead.indexOf("<")+2, dataRead.indexOf(">")-1);
        jTextArea1.append(dataPacket);
        jTextArea1.append("\n");
    }//GEN-LAST:event_jButton2ActionPerformed

